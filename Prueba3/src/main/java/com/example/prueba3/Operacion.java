package com.example.prueba3;

public class Operacion {

    // Los métodos que se requieren para crear la lógica del programa, deben estar aquí
    // Los métodos que se relacionan directamente con la interfaz y sirven para manipularla, deben estar en controller
    public int sumar(int a, int b) {
        return a+b;
    }

    public int restar(int a, int b) {
        return a-b;
    }

    public int multiplicar(int a, int b) {
        return a*b;
    }
}
